export function getVersesByThemes(themes: string[]): Verse[] {
  return verses.filter((v) =>
    v.themes.some((t) => themes.includes(t))
  );
}

export function getVerseById(id: string): Verse | undefined {
  return verses.find((v) => v.id === id);
}

export function searchVerses(query: string): Verse[] {
  const q = query.toLowerCase();
  return verses.filter(
    (v) =>
      v.surah.toLowerCase().includes(q) ||
      v.translation.toLowerCase().includes(q) ||
      v.themes.some((t) => t.toLowerCase().includes(q))
  );
}
